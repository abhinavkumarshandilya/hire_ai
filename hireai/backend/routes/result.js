const express = require("express");
const router  = express.Router();
const PDFKit  = require("pdfkit");
const Session   = require("../models/Session");
const Interview = require("../models/Interview");
const { protect, hrOnly } = require("../middleware/auth");

// HR stats
router.get("/stats", protect, hrOnly, async (req, res) => {
  try {
    const totalInterviews = await Interview.countDocuments({ createdBy: req.user._id });
    const allSessions = await Session.find({ status: "completed" })
      .populate({ path: "interview", match: { createdBy: req.user._id } });
    const valid  = allSessions.filter(s => s.interview);
    const short  = valid.filter(s => s.shortlisted).length;
    const avg    = valid.length ? +(valid.reduce((a, s) => a + s.scores.overall, 0) / valid.length).toFixed(1) : 0;
    res.json({ totalInterviews, totalCandidates: valid.length, shortlisted: short, avgScore: avg, selectionRate: valid.length ? +((short/valid.length)*100).toFixed(0) : 0 });
  } catch (e) { res.status(500).json({ message: e.message }); }
});

// Get session result
router.get("/session/:id", protect, async (req, res) => {
  try {
    const s = await Session.findById(req.params.id).populate("interview", "jobTitle jobDescription skills");
    if (!s) return res.status(404).json({ message: "Not found." });
    res.json(s);
  } catch (e) { res.status(500).json({ message: e.message }); }
});

// Download PDF report
router.get("/pdf/:sessionId", protect, async (req, res) => {
  try {
    const session = await Session.findById(req.params.sessionId).populate("interview", "jobTitle skills");
    if (!session) return res.status(404).json({ message: "Not found." });

    const doc = new PDFKit({ margin: 50, size: "A4" });
    res.setHeader("Content-Type", "application/pdf");
    res.setHeader("Content-Disposition", `attachment; filename=HireAI_${session.candidate.name.replace(/ /g, "_")}.pdf`);
    doc.pipe(res);

    // Header
    doc.rect(0, 0, 595, 80).fill("#6b5de6");
    doc.fillColor("white").font("Helvetica-Bold").fontSize(24).text("HireAI", 50, 25);
    doc.fontSize(11).font("Helvetica").text("Interview Report — Confidential", 50, 52);
    doc.fillColor("#1a1a18").moveDown(3);

    // Candidate info
    doc.font("Helvetica-Bold").fontSize(14).text("Candidate Information").moveDown(0.5);
    doc.font("Helvetica").fontSize(11)
      .text(`Name:      ${session.candidate.name}`)
      .text(`Email:     ${session.candidate.email}`)
      .text(`Role:      ${session.interview?.jobTitle || "N/A"}`)
      .text(`Date:      ${new Date(session.createdAt).toDateString()}`)
      .text(`Duration:  ${session.duration || "N/A"} min`)
      .moveDown();

    // Scores
    doc.font("Helvetica-Bold").fontSize(14).text("AI Score Summary").moveDown(0.5);
    doc.font("Helvetica").fontSize(11)
      .text(`Overall Score:        ${session.scores.overall}/10`)
      .text(`Technical Score:      ${session.scores.technical}/10`)
      .text(`Communication Score:  ${session.scores.communication}/10`)
      .text(`Confidence Score:     ${session.scores.confidence}/10`)
      .text(`Sentiment:            ${session.sentiment}`)
      .text(`AI Recommendation:    ${session.recommendation}`)
      .moveDown();

    // Summary
    doc.font("Helvetica-Bold").fontSize(14).text("AI Summary").moveDown(0.5);
    doc.font("Helvetica").fontSize(11).text(session.aiSummary || "No summary.", { align: "justify" }).moveDown();

    // Anti-cheat
    if (session.antiCheat.tabSwitches > 0 || session.antiCheat.faceNotDetected > 0) {
      doc.font("Helvetica-Bold").fontSize(14).fillColor("#dc2626").text("⚠ Anti-Cheat Flags").moveDown(0.5);
      doc.fillColor("#1a1a18").font("Helvetica").fontSize(11)
        .text(`Tab Switches:     ${session.antiCheat.tabSwitches}`)
        .text(`Face Not Detected:${session.antiCheat.faceNotDetected}`)
        .text(`Multiple Persons: ${session.antiCheat.multiplePersons}`)
        .moveDown();
    }

    // Answers
    doc.font("Helvetica-Bold").fontSize(14).fillColor("#1a1a18").text("Question-by-Question Breakdown").moveDown(0.5);
    session.answers.forEach((a, i) => {
      if (doc.y > 700) doc.addPage();
      doc.font("Helvetica-Bold").fontSize(11).text(`Q${i + 1} [${a.questionType}]: ${a.questionText}`);
      doc.font("Helvetica").fontSize(10)
        .text(`Answer:   ${a.answerText || "No answer provided"}`)
        .fillColor("#6b5de6").text(`Score:    ${a.aiScore}/10`)
        .fillColor("#1a1a18").text(`Feedback: ${a.aiFeedback}`)
        .moveDown(0.5);
    });

    doc.fontSize(9).fillColor("#a3a39d").text("Generated by HireAI Platform", { align: "center" });
    doc.end();
  } catch (e) { res.status(500).json({ message: e.message }); }
});

module.exports = router;
